	<div class="menu_header"><h5>African Cuisine</h5></div>
	<img src="./Images/1.jpg" style="float:left;margin:10px 10px 10px;border-radius:7px;" width="300" height="230"> 
	<h2>
	
	<p>African cuisine is a generalized term collectively referring to the cuisines of Africa. 
	The continent of Africa is the second largest landmass on Earth, and is home to hundreds 
	of different cultural and ethnic groups. This diversity is also reflected in the many local 
	culinary traditions in terms of choice of ingredients, style of preparation and cooking techniques.</p>

<p>Traditionally, the various cuisines of Africa use a combination of locally available fruits,
 cereal grains and vegetables, as well as milk and meat products. In some parts of the continent,
 the traditional diet features a preponderance of milk, curd and whey products. In much of Tropical Africa,
 however, cow's milk is rare and cannot be produced locally (owing to various diseases that affect livestock). 
 Depending on the region, there are also sometimes quite significant differences in the eating and drinking habits
 and proclivities throughout the continent's many populations: Central Africa, East Africa, the Horn of Africa, North Africa,
 Southern Africa and West Africa each have their own distinctive dishes, preparation techniques, and consumption mores.</p>

 </h2>
 <div class="menu_header"><h5>Central African Cuisine</h5></div>
	<img src="./Images/1.jpg" style="float:right;margin:10px 10px 10px;border-radius:7px;" width="300" height="230"> 
	<h2>
	
	<p>Central Africa stretches from the Tibesti Mountains in the north to vast rainforest basin of the Congo River, and has remained 
	largely free of culinary influences of the outside world, until the late 19th century, with the exception of the widespread adaptation 
	of cassava, peanut, and Chile pepper plants which arrived along with the slave trade during the early 16th century. These foodstuffs have 
	had a large influence on the local cuisine, perhaps less on the preparation methods. Central African cooking has remained mostly traditional.
	Nevertheless, like other parts of Africa, Central African cuisine also presents an array of dishes.</p>

<p>The basic ingredients are plantains and cassava. Fufu-like starchy foods (usually made from fermented cassava roots) are served with grilled meat and sauces. 
A variety of local ingredients are used while preparing other dishes like spinach stew, cooked with tomato, peppers, chillis, onions, and peanut butter. 
Cassava plants are also consumed as cooked greens. Groundnut (peanut) stew is also prepared, containing chicken, okra, ginger, and other spices.
 Another favorite is Bambara, a porridge of rice, peanut butter and sugar. Beef and chicken are favorite meat dishes, but game meat preparations
 containing crocodile, monkey, antelope and warthog are also served occasionally.</p>

 </h2>
 
 
 <div class="menu_header"><h5>East African Cuisine</h5></div>
	<img src="./Images/1.jpg" style="float:left;margin:10px 10px 10px;border-radius:7px;" width="300" height="230"> 
	<h2>
	
	<p>The cuisine of East Africa varies from area to area. In the inland savannah, the traditional cuisine of cattle-keeping peoples is 
	distinctive in that meat products are generally absent. Cattle, sheep and goats were regarded as a form of currency and a store of wealth, 
	and are not generally consumed as food. In some areas, traditional peoples consume the milk and blood of cattle, but rarely the meat. Elsewhere,
	other peoples are farmers who grow a variety of grains and vegetables. Maize (corn) is the basis of ugali, the East African version of 
	West Africa's fufu. Ugali is a starch dish eaten with meats or stews. In Uganda, steamed, green bananas called matoke provide the starch filler of many meals.</p>

<p>Around 1000 years ago, the Arabs settled in the coastal areas of East Africa, and Arabic influences are especially reflected in the Swahili 
cuisine of the coast – steamed cooked rice with spices in Persian style, use of saffron, cloves, cinnamon and several other spices, and pomegranate juice.</p>

<p>Several centuries later, the British and the Indians came, and both brought with them their foods, like Indian spiced vegetable curries,
 lentil soups, chapattis and a variety of pickles. Just before the British and the Indians, the Portuguese had introduced techniques of 
 roasting and marinating, as also use of spices turning the bland diet into aromatic stewed dishes. Portuguese also brought from their Asian colonies
 fruits like the orange, lemon and lime. From their colonies in the New World, Portuguese also brought exotic items like chilies, peppers, maize, tomatoes, 
 pineapple, bananas, and the domestic pig – now, all these are common elements of East African foods.</p>
 </h2>

 
  <div class="menu_header"><h5>Horn of Africa</h5></div>
	<img src="./Images/1.jpg" style="float:left;margin:10px 10px 10px;border-radius:7px;" width="300" height="230"> 
	<h2>
	
	<p>The main traditional dishes in Ethiopian cuisine and Eritrean cuisine are tsebhis (stews) served with injera[1] 
	(flatbread made from teff,[1] wheat, or sorghum), and hilbet (paste made from legumes, mainly lentil, faba beans).
	Eritrean and Ethiopian cuisine (especially in the northern half) are very similar, given the shared history of the two countries.</p>

<p>Eritrean and Ethiopian food habits vary regionally. In the highlands, injera is the staple diet and is eaten daily among the Tigrinya. 
Injera is made out of a variation and/or blend of: teff, wheat, barley, sorghum and corn and resembles a spongy, slightly sour pancake.
 When eating, diners generally share food from a large tray placed in the centre of a low dining table. Numerous injera are layered on this 
 tray and topped with various spicy stews. Diners then break into the section of injera in front of them, tearing off pieces and dipping them into the stews.</p>

<p>In the lowlands, the main dish is akelet, a porridge-like dish made from wheat flour dough. A ladle is used to scoop out the top, which is
 filled with berbere and butter sauce and surrounded by milk or yoghurt. A small piece of dough is broken and then used to scoop up the sauce.
 The best known Ethio-Eritrean cuisine consists of various vegetable or meat side dishes and entrées, usually a wat, or thick stew, served atop injera, 
 a large sourdough flatbread made of teff flour. One does not eat with utensils, but instead uses injera to scoop up the entrées and side dishes.</p>

<p>Tihlo prepared from roasted barley flour is very popular in Amhara, Agame, and Awlaelo (Tigrai). Traditional Ethiopian cuisine employs no pork or shellfish 
of any kind, as they are forbidden in the Islamic, Jewish, and Ethiopian Orthodox Christian faiths. It is also very common to eat from the same dish in the center
 of the table with a group of people.</p>
 
 <p>Somalian cuisine varies from region to region and consists of an exotic mixture of diverse culinary influences. It is the product of Somalia's rich tradition
 of trade and commerce. Despite the variety, there remains one thing that unites the various regional cuisines: all food is served halal. There are therefore no 
 pork dishes, alcohol is not served, nothing that died on its own is eaten, and no blood is incorporated. Qaddo or lunch is often elaborate.</p>

<p>Varieties of bariis (rice), the most popular probably being basmati, usually serve as the main dish. Spices like cumin, cardamom, cloves, cinnamon and sage are 
used to aromatize these different rice dishes. Somalis serve dinner as late as 9 pm. During Ramadan, dinner is often served after Tarawih prayers – sometimes as late as 11 pm.</p>

<p>Xalwo (halwo) or halva is a popular confection served during special occasions such as Eid celebrations or wedding receptions. It is made from sugar, cornstarch,
 cardamom powder, nutmeg powder, and ghee. Peanuts are also sometimes added to enhance texture and flavor.[2] After meals, homes are traditionally perfumed using
 frankincense (lubaan) or incense (cuunsi), which is prepared inside an incense burner referred to as a dabqaad.</p>
 </h2>
 
 
 

	
